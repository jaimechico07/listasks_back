import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        trim: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
},{
    timestamps: true, //para que se registre la fecha de creacion y actualizacion del usuario
})


export default mongoose.model('User', userSchema)
